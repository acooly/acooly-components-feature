import com.acooly.core.utils.enums.Messageable;

import java.util.ArrayList;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;

public enum AccessTypeEnum implements Messageable {
    LOCAL_STORAGE("LOCAL_STORAGE","本地存储"),
    OBS_PUBLIC("OBS_PUBLIC","OBS公网访问"),
    OBS_STS("OBS_STS","OBS_STS令牌访问"),
    ;
    private final String code;
    private final String message;

    AccessTypeEnum(String code, String message) {
        this.code = code;
        this.message = message;
    }

    public String getCode() {
        return code;
    }

    public String getMessage() {
        return message;
    }

    @Override
    public String code() {
        return code;
    }

    @Override
    public String message() {
        return message;
    }

    public static Map<String, String> mapping() {
        Map<String, String> map = new LinkedHashMap<String, String>();
        for (AccessTypeEnum type : values()) {
            map.put(type.getCode(), type.getMessage());
        }
        return map;
    }

    /**
     * 通过枚举值码查找枚举值。
     *
     * @param code 查找枚举值的枚举值码。
     * @return 枚举值码对应的枚举值。
     * @throws IllegalArgumentException 如果 code 没有对应的 Status 。
     */
    public static AccessTypeEnum find(String code) {
        for (AccessTypeEnum status : values()) {
            if (status.getCode().equals(code)) {
                return status;
            }
        }
        return null;
    }

    /**
     * 获取全部枚举值。
     *
     * @return 全部枚举值。
     */
    public static List<AccessTypeEnum> getAll() {
        List<AccessTypeEnum> list = new ArrayList<AccessTypeEnum>();
        for (AccessTypeEnum status : values()) {
            list.add(status);
        }
        return list;
    }

    /**
     * 获取全部枚举值码。
     *
     * @return 全部枚举值码。
     */
    public static List<String> getAllCode() {
        List<String> list = new ArrayList<String>();
        for (AccessTypeEnum status : values()) {
            list.add(status.code());
        }
        return list;
    }

}
